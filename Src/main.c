/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {
	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USART2_UART_Init();
	MX_SPI1_Init();
	MX_ADC1_Init();
	MX_ADC2_Init();
	/* USER CODE BEGIN 2 */

	// NRF address for pairing
	uint8_t tx_addr[] = { 0xEE, 0xDD, 0xCC, 0xBB, 0xAA };

	// Initialization of NRF
	nrf24_init();
	nrf24_tx_mode(tx_addr, 30);

	// Variables for sending data
	uint8_t head_position;
	uint8_t speed;
	uint8_t x_joystick;
	uint8_t y_joystick;
	uint8_t mode;
	char MSG[100] = { '\0' };
	char MSG2[40] = { '\0' };
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1) {
		// Get value from head slider and add it to the data string
		ADC1_Select_CH1();
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
		head_position = HAL_ADC_GetValue(&hadc1);				// [104 - 251]
		HAL_ADC_Stop(&hadc1);
		head_position = map(head_position, 104, 251, 42, 200);
		if (head_position < 100) {
			// 2 digit nums into 3 digit nums (42 -> 042)
			sprintf(MSG, "h0%hu", head_position);
		} else {
			sprintf(MSG, "h%hu", head_position);
		}

		// Get value from speed slider and add it to the data string
		ADC2_Select_CH2();
		HAL_ADC_Start(&hadc2);
		HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
		speed = HAL_ADC_GetValue(&hadc2);						// [104 - 251]
		HAL_ADC_Stop(&hadc2);
		speed = map(speed, 104, 251, 0, 100);
		if (speed < 100 && speed > 9) {
			// 2 digit nums into 3 digit nums (42 -> 042)
			sprintf(MSG2, "s0%hu", speed);
		} else if (speed < 10) {
			// 1 digit nums into 3 digit nums (2 -> 002)
			sprintf(MSG2, "s00%hu", speed);
		} else {
			sprintf(MSG2, "s%hu", speed);
		}
		strcat(MSG, MSG2);

		// Get value from y-potentiometer of joystick and add it to the data string
		ADC1_Select_CH15();
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
		y_joystick = HAL_ADC_GetValue(&hadc1);						// [0 - 251]
		HAL_ADC_Stop(&hadc1);
		y_joystick = map(y_joystick, 0, 251, 0, 200);
		if (y_joystick < 100 && y_joystick > 9) {
			// 2 digit nums into 3 digit nums (42 -> 042)
			sprintf(MSG2, "y0%hu", y_joystick);
		} else if (y_joystick < 10) {
			// 1 digit nums into 3 digit nums (2 -> 002)
			sprintf(MSG2, "y00%hu", y_joystick);
		} else {
			sprintf(MSG2, "y%hu", y_joystick);
		}
		strcat(MSG, MSG2);

		// Get value from x-potentiometer of joystick and add it to the data string
		ADC2_Select_CH17();
		HAL_ADC_Start(&hadc2);
		HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
		x_joystick = HAL_ADC_GetValue(&hadc2);						// [0 - 251]
		HAL_ADC_Stop(&hadc2);
		x_joystick = map(x_joystick, 0, 251, 0, 200);
		if (x_joystick < 100 && x_joystick > 9) {
			// 2 digit nums into 3 digit nums (42 -> 042)
			sprintf(MSG2, "x0%hu", x_joystick);
		} else if (x_joystick < 10) {
			// 1 digit nums into 3 digit nums (2 -> 002)
			sprintf(MSG2, "x00%hu", x_joystick);
		} else {
			sprintf(MSG2, "x%hu", x_joystick);
		}
		strcat(MSG, MSG2);

		// Get value from button, add it to the data string and transmit all of the data
		mode = HAL_GPIO_ReadPin(mode_button_GPIO_Port, mode_button_Pin);
		sprintf(MSG2, "m%hu\r\n\n", mode);							// [0 - 1]
		strcat(MSG, MSG2);
		if (nrf24_transmit((uint8_t*) MSG)) {
			HAL_GPIO_TogglePin(green_led_GPIO_Port, green_led_Pin);
		}

		HAL_Delay(100);

		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
	}
	/* USER CODE END 3 */
}

